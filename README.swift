/*
An example of MVVM with Coordinator/Combine Pattern
 Karin Prater
 https://www.youtube.com/watch?v=wpw3l_jTuOo
 
*/

This is an exploration of the MVVM pattern with the Coordinator pattern and Combine framework in SwiftUI. 
The goal is to create a simple app that demonstrates how to use these patterns together to create a clean and maintainable codebase
that is developed using a mix of SwiftUI and UIKit.

The app will consist of a simple list of items that can be selected to view more details. 

The app will use a 
1) Coordinator to manage the navigation between the list and detail views, and 
2) Combine to handle data binding and updates.

